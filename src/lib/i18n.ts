export type TranslationKeys = (typeof translations)[Locale];

/**
 * Get translations for a specific locale
 */
export function getTranslations(locale: Locale): TranslationKeys {
  return translations[locale] ?? translations.fr;
}

/**
 * Get a nested translation value by dot-separated key path
 * e.g. getNestedTranslation(trans, 'nav.home') => 'Accueil'
 */
export function getNestedTranslation(
  trans: TranslationKeys,
  keyPath: string
): string {
  const keys = keyPath.split('.');
  let current: any = trans;
  for (const key of keys) {
    if (current && typeof current === 'object' && key in current) {
      current = current[key];
    } else {
      return keyPath; // fallback to key path
    }
  }
  return typeof current === 'string' ? current : keyPath;
}
